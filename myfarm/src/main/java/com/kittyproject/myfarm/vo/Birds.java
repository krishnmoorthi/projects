package com.kittyproject.myfarm.vo;

/**
 * @author kthangav
 *
 */
public class Birds {

	private String type;
	private long total;
	private long eggsAvailable;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public long getTotal() {
		return total;
	}
	public void setTotal(long total) {
		this.total = total;
	}
	public long getEggsAvailable() {
		return eggsAvailable;
	}
	public void setEggsAvailable(long eggsAvailable) {
		this.eggsAvailable = eggsAvailable;
	}
	
}
